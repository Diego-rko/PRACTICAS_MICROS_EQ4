				INCLUDE <E:\Usuarios\Videos\Radeon ReLive\micro\EJERCICIOS\ENCABEZADO.asm>
				CLRF	TRISC				;SALIDA PC
				BCF		TRISB,7				;SALIDA B7
				BCF		STATUS,RP0			;B0
				CLRF	PORTC				;LIMPIAR PC
				BCF		PORTB,7				;LIMPIAR B7


;----------------------------DIVISION----------------------------------------
MULTI_O			EQU		0X21		   		;DATO A SUMAR (MULTIPLICANDO).
MULTI_R   		EQU		0X22		    	;VECES A SUMAR(MULTIPLICADOR).
SUMAT			EQU		0X23		    	;SUMA DE LOS DATOS

NUMERADOR	        EQU     0X31                
DENOMINADOR  		EQU		0X32		    	
COCIENTE			EQU		0X33		    	
;----------------------------------------------------------------------------------
				CLRF	NUMERADOR
				CLRF	DENOMINADOR
				CLRF	COCIENTE
				CLRF	MULTI_O	    		;LIMPIAR DATOA SUMAR.
				CLRF	MULTI_R	   			;LIMPIAR VECES A SUMAR.
				CLRF	SUMAT		   		;LIMPIAR SUMA TOTAL.


CICLO			BTFSS	PORTB,0				;B0=1?
				GOTO	CICLO				
				MOVF	PORTD,W				
				MOVWF	PORTC				;C=D(DATO 1)
				MOVWF	0X20				;D(DATO 1) SE GUARDA EN 0X20


REPETIRB1		BTFSS	PORTB,1				;B1=1?
				GOTO	REPETIRB2			
				GOTO	SUMA

REPETIRB2		BTFSS	PORTB,2				;B2=1?
				GOTO	REPETIRB3
				GOTO	RESTA

REPETIRB3		BTFSS	PORTB,3				;B3=1?
				GOTO	REPETIRB1
				GOTO	MULTIPLICACION

;--------------------------SUMA---------------------------------------------
SUMA			MOVF	PORTD,W				;LEE PD Y GUARDA EN W
				MOVWF	0X25				;D(DATO 2) LO GUARDA EN 0X25
				ADDWF	0X20,W				;SUMA D(DATO 1)+D(DATO 2)
				BTFSS	STATUS,C			;C=1?
				GOTO	MOSTRAR				;MOSTRAR POR PC AL FINAL
				BSF		PORTB,7				;B7 ENCIENDE
				GOTO	MOSTRAR					;MOSTRAR POR PC AL FINAL
;--------------------------RESTA--------------------------------------------
RESTA			MOVF	PORTD,W				;LEE PD Y GUARDA EN W
				MOVWF	0X26				;D(DATO 2) LO GUARDA EN 0X26
				SUBWF	0X20,W				;RESTA D(DATO 1)-D(DATO 2)
				BTFSS	STATUS,C			;C=1?
				GOTO	MAGNITUD		
				GOTO	MOSTRAR				;MOSTRAR POR PC AL FINAL
MAGNITUD		SUBLW	0X00				;PC=|PD(DATO 1)-PD(DATO 2)|
				BSF		PORTB,7				;MOSTRAR POR PC AL FINAL
				GOTO	MOSTRAR			

;----------------------------MULTIPLICACION-----------------------------------

;------------------------------------------------------------------------

MULTIPLICACION	MOVF	PORTD,W
				MOVWF	0X27				;D(DATO 2) LO GUARDA EN 0X27
				MOVWF	MULTI_R				;MULIT_R A W
				MOVF	0X20,W		    	;MOVEMOS MULTI_R A 0X20.
				MOVWF	MULTI_O			    ;MUEVE W A REG MULTI_O
				CLRF	W					;LIMPIAMOS ACUMULADOR
				MOVF	MULTI_O,F			;F=MULTI_O
				BTFSS	STATUS,Z	    	;Z=0?.
CONDICION2:		GOTO	CONDICION1	
				GOTO	MOSTRAR				;MOSTRAR POR PC AL FINAL
CONDICION1:		MOVF	MULTI_R,F
				BTFSS	STATUS,Z	    	;MULTI_R = 0?.
				GOTO	MULTIPLICAR
				GOTO	CONDICION3

MULTIPLICAR		MOVF	MULTI_O,W   		;MUEVE MULTI_O A WREG.
				ADDWF	SUMAT,F		    	;SUMAT = SUMAT + MULTI_O.
				BTFSC	STATUS,C	    	;C=O?.
				BSF		PORTB,7
				DECF	MULTI_R,F	    	;DECREMENTA EN 1 Y GUARDA EN EL MISMO.
				GOTO	CONDICION2
CONDICION3:		MOVF	SUMAT,W		    	;MUEVE ST A WREG.
				GOTO	MOSTRAR				;MOSTRAR POR PC AL FINAL
				




MOSTRAR			MOVWF	PORTC		    ; SALIDA DE WREG A PC.		
				BTFSS	PORTB,0		    ; PB.0 = 1?.
				GOTO	MOSTRAR
				MOVF	0X20,0		    ; MUEVE A WREG.
				MOVWF	NUMERADOR	    ; MUEVE A NUMERADOR.
				MOVF	0X30,0		    ; MUEVE A WREG.
				MOVWF	DENOMINADOR		    ; MUEVE A DENOMINADOR.
				CLRF	W		    	; LIMPIA WREG.
				MOVF	DENOMINADOR,1	    
				BTFSS	STATUS,Z	    ; DENOMINADOR=0?.
				GOTO 	DIVIDIENDO
				GOTO	MIMIR				;MOSTRAR POR PC AL FINAL


DIVIDIENDO		MOVF	DENOMINADOR,0
				SUBWF	NUMERADOR,0	    	;WREG=DENOMINADOR-NUMERADOR.
				BTFSC	STATUS,C	   		;C=0? NUMERADOR=<DENOMINADOR?.
				GOTO	DIVIDIR				;MOSTRAR POR PC AL FINAL
				GOTO	DIV_ACA



DIVIDIR			MOVF	DENOMINADOR,W			;MUEVE NUMERADOR A W
				SUBWF   NUMERADOR,F			; NUMERADOR = NUMERADOR - DENOMINADOR
				INCF	COCIENTE,F			; AGREGA UN 1 AL COCIENTE
				GOTO 	DIVIDIENDO


				 
DIV_ACA			MOVF	NUMERADOR,1			;GUARDA NUMERADOR EN W
				BTFSS	STATUS,Z			;¿NUMERADOR =0?
				BSF		PORTB,7				;FLAG ACARREO
				MOVF	COCIENTE,0			;MOVER COCIENTE A W
				GOTO 	MIMIR				;MOSTRAR POR PC AL FINAL

;------------------------------------------------------------------------------				
MIMIR			MOVWF	PORTC

				GOTO 	$
				END